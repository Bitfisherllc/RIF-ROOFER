// Hash function to consistently map guide slugs to icons
function hashString(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  return Math.abs(hash);
}

/**
 * Get a unique icon for a guide based on its slug
 * This ensures each guide gets a consistent, unique icon
 */
export function getGuideIcon(slug: string) {
  const hash = hashString(slug);
  const iconIndex = hash % iconPool.length;
  return iconPool[iconIndex];
}

/**
 * Get icon metadata for SEO purposes
 */
export function getGuideIconMetadata(slug: string, title: string) {
  const icon = getGuideIcon(slug);
  return {
    icon,
    alt: `${title} - Guide Icon`,
    title: title,
    ariaLabel: `Icon representing ${title}`,
  };
}
